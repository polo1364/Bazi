function listText(items: string[], empty = '此項資料尚未完整產生，暫不作進一步延伸。'): string {
  return items.length ? items.join('、') : empty
}

function cleanPlainText(text: string): string {
  return text
    .replace(/癸水日主，也就是癸水日主/g, '癸水日主')
    .replace(/日主為癸水，也就是癸水日主/g, '日主為癸水')
    .replace(/你屬於癸水日主，核心像細水、雨露/g, '你的核心特質像細水、雨露')
    .replace(/喜用金、水，也就是喜用金、水/g, '喜用金、水')
    .replace(/系統初步判斷，金和水是比較適合拿來補強的方向。這次報告列出的方向是金、水。/g, '系統初步判斷，金和水是比較適合拿來補強的方向。')
    .replace(/報告建議喜用金、水，白話就是喜用金、水/g, '報告建議用金、水作為主要補強方向')
    .replace(/喜用金、水，白話就是喜用金、水/g, '金和水是比較適合拿來補強的方向')
    .replace(/白話說，不是全好或全壞，而是有些地方順、有些地方需要靠後天調整。白話說，姓名學不是單純大吉，也不是單純不好，而是有幫助的地方，也有需要靠後天調整的地方。/g, '簡單說，這不是全好或全壞，而是有些地方順、有些地方需要靠後天調整。')
    .replace(/天人格相剋，基礎與發展受牽制/g, '姓名結構裡有支援，也有拉扯，不是全好或全壞，需要靠後天調整')
    .replace(/後天調整。。/g, '後天調整。')
    .replace(/。{2,}/g, '。')
    .replace(/也就是([^，。]+)，也就是\1/g, '也就是$1')
    .replace(/宜/g, '建議')
}

function section(heading: string, body: string) {
  return { heading, body: cleanPlainText(body) }
}

