export function sanitizeAiNarrativeObject<T>(value: T): T {
  if (typeof value === 'string') {
    return sanitizeAiNarrativeText(value) as T
  }

  if (Array.isArray(value)) {
    return value.map((item) => sanitizeAiNarrativeObject(item)) as T
  }

  if (value && typeof value === 'object') {
    return Object.fromEntries(
      Object.entries(value).map(([key, val]) => [key, sanitizeAiNarrativeObject(val)]),
    ) as T
  }

  return value
}
